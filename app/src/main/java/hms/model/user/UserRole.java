package hms.model.user;

/** Enumerates the different roles a user can have within the Hospital Management System. */
public enum UserRole {
    /** Represents a patient within the system. */
    PATIENT,

    /** Represents a doctor within the system. */
    DOCTOR,

    /** Represents a pharmacist within the system. */
    PHARMACIST,

    /** Represents an administrator within the system. */
    ADMINISTRATOR
}
